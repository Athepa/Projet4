    <h3>Je vous présente mon nouveau roman publié en ligne</h3>
    <p>L'écriture d'un roman peut vous plonger dans une solitude dont seule l'achèvement de celui-ci peut vous sortir. 
        Quand j'ai décidé de l'écriure de ce nouveau roman, je me suis posé une question: comment mener ce projet à son terme sans subir cette solitude?
        Et c'est là que ma file aînée m'a suggéré une idée incroyable: "Papa pourquoi ne pas publier ton roman sur internet et interagir avec tes lecteurs?"</br>
        Eurêka!</br>
        J'ai donc décidé de publier ce roman sur ce blog sous forme d'épisodes, chaque chapitre représentant un épisode. Vous pourrez ainsi accéder aux différents épisodes, 
        me laisser vos impressions et me proposer des idées d'amélioration et pourquoi pas de nouveaux personnages.</br>
        Vous l'aurez compris, ce roman, c'est avec vous que je vais l'écrire. Ce sera ma façon de vous remercier de votre indéfectible loyauté durant toutes ces années.
    </p>
